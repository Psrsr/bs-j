// 旋转240度之后的x坐标
function x_revolve(x, y, angle) {
  //计算弧度
  // let radian=2*Math.PI/360
  return Math.cos((2 * Math.PI * angle) / 360) * x - Math.sin((2 * Math.PI * angle) / 360) * y;
}

// 旋转240度之后的y坐标
function y_revolve(x, y, angle) {
  //计算弧度
  // let radian=2*Math.PI/360
  return Math.cos((2 * Math.PI * angle) / 360) * y + Math.sin((2 * Math.PI * angle) / 360) * x;
}

